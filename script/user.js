class User{
    set name(name){
        this._name = name
    }
    get name(){
        return this._name
    }
    set birthdate(birthdate){
        this._birthdate = birthdate
    }
    get birthdate(){
        return this._birthdate
    }
    set email(email){
        this._email = email
    }
    get email(){
        return this._email
    }
    set phone_number(phone_number){
        this._phone_number = phone_number
    }
    get phone_number(){
        return this._phone_number
    }
}
